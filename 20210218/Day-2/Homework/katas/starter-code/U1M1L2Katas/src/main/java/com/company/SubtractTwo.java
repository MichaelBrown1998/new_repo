package com.company;
import java.util.Scanner;
public class SubtractTwo {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        int Subtracttwo = Integer.parseInt(scanner.nextLine());

        int subtract = Integer.parseInt(scanner.nextLine());

        System.out.println(Subtracttwo - subtract);

    }
}
